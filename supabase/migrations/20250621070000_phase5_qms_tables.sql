-- =============================================
-- RLS Policies: audit_plans
-- Via owning checklist's user_id; admin sees all
-- =============================================
create policy audit_plans_select on public.audit_plans
    for select to authenticated
    using (
        exists (
            select 1 from public.audit_checklists c
            where c.id = audit_plans.checklist_id
            and (c.user_id = auth.uid() or exists (
                select 1 from public.accounts a
                where a.id = auth.uid() and a.role = 'admin'
            ))
        )
    );

create policy audit_plans_insert on public.audit_plans
    for insert to authenticated
    with check (
        exists (
            select 1 from public.audit_checklists c
            where c.id = audit_plans.checklist_id
            and c.user_id = auth.uid()
        )
    );

create policy audit_plans_delete on public.audit_plans
    for delete to authenticated
    using (
        exists (
            select 1 from public.audit_checklists c
            where c.id = audit_plans.checklist_id
            and (c.user_id = auth.uid() or exists (
                select 1 from public.accounts a
                where a.id = auth.uid() and a.role = 'admin'
            ))
        )
    );

